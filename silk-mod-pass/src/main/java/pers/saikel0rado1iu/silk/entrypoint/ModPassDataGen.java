package pers.saikel0rado1iu.silk.entrypoint;

import net.fabricmc.fabric.api.datagen.v1.DataGeneratorEntrypoint;
import net.fabricmc.fabric.api.datagen.v1.FabricDataGenerator;

/**
 * <h2>数据生成器</h2>
 * 用于 Silk API: Mod Pass 的数据生成器
 *
 * @author <a href="https://github.com/Saikel-Orado-Liu">
 *         <img alt="author" src="https://avatars.githubusercontent.com/u/88531138?s=64&v=4">
 *         </a>
 * @since 0.1.0
 */
public final class ModPassDataGen implements DataGeneratorEntrypoint {
    @Override
    public void onInitializeDataGenerator(FabricDataGenerator fabricDataGenerator) {
        FabricDataGenerator.Pack pack = fabricDataGenerator.createPack();
        pack.addProvider(ModPassI18nProvider.EN_US::provider);
        pack.addProvider(ModPassI18nProvider.ZH_CN::provider);
        pack.addProvider(ModPassI18nProvider.ZH_HK::provider);
        pack.addProvider(ModPassI18nProvider.ZH_TW::provider);
    }
}
